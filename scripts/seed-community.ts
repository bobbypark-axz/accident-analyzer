// ─── 메인 실행 ───
async function main() {
  console.log('🚀 커뮤니티 더미데이터 시드 시작');
  console.log(`📋 시나리오 ${scenarios.length}개 × 2건 = ${scenarios.length * 2}건 게시물 생성 예정\n`);

  let postCount = 0;
  let commentCount = 0;
  let failCount = 0;

  for (const s of scenarios) {
    for (let i = 0; i < s.descriptions.length; i++) {
      const desc = s.descriptions[i];
      const analysis = {
        summary: s.summary,
        chartCode: s.code,
        chartName: s.name,
        ratio: {
          a: { label: s.aLabel, percent: s.a },
          b: { label: s.bLabel, percent: s.b },
          reason: s.reason,
        },
        laws: s.laws,
        cases: s.cases,
        notes: s.notes,
        needed: [],
      };

      // 제목: description 첫 문장
      const title = desc.match(/^(.+?)[.。ㅠ~!?]/)?.[1]?.slice(0, 35) || desc.slice(0, 30);
      const createdAt = randomPastDate(60);

      const { data: post, error } = await supabase
        .from('posts')
        .insert({
          nickname: nickname(),
          analysis,
          title,
          summary: s.summary,
          description: desc,
          fault_ratio_a: s.a,
          fault_ratio_b: s.b,
          chart_code: s.code,
          session_token: crypto.randomUUID(),
          view_count: randInt(15, 850),
          created_at: createdAt,
        })
        .select()
        .single();

      if (error || !post) {
        console.error(`  ❌ ${s.code} #${i + 1} 실패:`, error?.message);
        failCount++;
        continue;
      }

      postCount++;
      process.stdout.write(`  ✅ ${s.code} #${i + 1} "${title.slice(0, 20)}..."`);

      // 댓글 1~4개
      const numComments = randInt(1, 4);
      const usedComments = new Set<string>();
      for (let c = 0; c < numComments; c++) {
        let content = pick(COMMENT_TEMPLATES);
        let attempts = 0;
        while (usedComments.has(content) && attempts < 5) {
          content = pick(COMMENT_TEMPLATES);
          attempts++;
        }
        usedComments.add(content);

        const commentCreatedAt = new Date(
          new Date(createdAt).getTime() + randInt(3600000, 5 * 86400000),
        ).toISOString();

        const { error: cErr } = await supabase.from('comments').insert({
          post_id: post.id,
          nickname: nickname(),
          content,
          session_token: crypto.randomUUID(),
          created_at: commentCreatedAt,
        });

        if (!cErr) commentCount++;
      }

      // 좋아요 랜덤 0~25개
      const numLikes = randInt(0, 25);
      for (let l = 0; l < numLikes; l++) {
        await supabase.from('likes').insert({
          post_id: post.id,
          session_token: crypto.randomUUID(),
        });
      }

      console.log(` (댓글 ${numComments}, 좋아요 ${numLikes})`);
    }
  }

  console.log('\n🎉 완료');
  console.log(`   게시물: ${postCount}건 (실패 ${failCount}건)`);
  console.log(`   댓글: ${commentCount}건`);
}

main().catch((e) => {
  console.error('💥 실행 실패:', e);
  process.exit(1);
});
