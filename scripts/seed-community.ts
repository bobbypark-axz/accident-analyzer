// 지난 N일 사이 랜덤 시각 (최근일수록 약간 더 많게)
const randomPastDate = (maxDaysAgo = 60): string => {
  const bias = Math.pow(Math.random(), 1.6); // 0~1, 0쪽(=최근)에 편향
  const daysAgo = bias * maxDaysAgo;
  const ms = Date.now() - daysAgo * 24 * 60 * 60 * 1000 - randInt(0, 86400000);
  return new Date(ms).toISOString();
};

