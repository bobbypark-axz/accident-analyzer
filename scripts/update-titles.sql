-- 기존 게시물 title을 chartCode 기반 사고 요약 제목으로 일괄 업데이트
-- 실행: Supabase Dashboard → SQL Editor → New Query → 아래 전체 붙여넣기 → Run
-- 영향: chart_code가 매핑 테이블에 있는 모든 posts 행

UPDATE posts
SET title = CASE chart_code
  -- 신호교차로
  WHEN '차1-1' THEN '교차로 직진 양측 녹색 충돌 (차1-1)'
  WHEN '차1-2' THEN '신호위반 직진 충돌 (차1-2)'
  WHEN '차2-1' THEN '교차로 직진 vs 우회전 충돌 (차2-1)'
  WHEN '차2-2' THEN '교차로 직진 vs 정상 우회전 (차2-2)'
  WHEN '차3-1' THEN '좌회전 신호 중 신호위반 직진 충돌 (차3-1)'
  WHEN '차3-2' THEN '비보호 좌회전 vs 직진 충돌 (차3-2)'
  WHEN '차4-1' THEN '동시 좌회전 교차 충돌 (차4-1)'
  WHEN '차4-2' THEN '좌회전 vs 유턴 충돌 (차4-2)'
  WHEN '차5-1' THEN '우회전 중 횡단보도 보행자 사고 (차5-1)'

  -- 비신호교차로
  WHEN '차10-1' THEN '비신호 교차로 직진 충돌 (차10-1)'
  WHEN '차11-1' THEN '비신호 교차로 직진 vs 우회전 (차11-1)'
  WHEN '차11-2' THEN '비신호 교차로 대로 vs 소로 진입 (차11-2)'
  WHEN '차12-1' THEN '비신호 교차로 직진 vs 좌회전 (차12-1)'
  WHEN '차13-1' THEN '비신호 교차로 좌회전 vs 직진 (차13-1)'
  WHEN '차14-1' THEN '비신호 교차로 우회전 vs 좌회전 (차14-1)'
  WHEN '차15-1' THEN '비신호 교차로 동시 좌회전 (차15-1)'
  WHEN '차16-1' THEN '유턴 중 직진차 충돌 (차16-1)'
  WHEN '차16-2' THEN '유턴금지 구간 불법 유턴 사고 (차16-2)'
  WHEN '차17-1' THEN '비신호 교차로 우회전 후방 추돌 (차17-1)'
  WHEN '차20-1' THEN 'T자 교차로 직진 vs 좌회전 (차20-1)'
  WHEN '차21-1' THEN 'T자 교차로 좌회전 vs 직진 (차21-1)'

  -- 도로 외
  WHEN '차31-1' THEN '주차장 진출 vs 본선 직진 (차31-1)'
  WHEN '차31-2' THEN '주차장 진입 중 후방 추돌 (차31-2)'

  -- 동일방향
  WHEN '차41-1' THEN '전방주시 태만 후행 추돌 (차41-1)'
  WHEN '차42-1' THEN '선행사고 정차 차량 추돌 (차42-1)'
  WHEN '차42-2' THEN '급 진로변경 끼어들기 사고 (차42-2)'
  WHEN '차42-3' THEN '동시 진로변경 교차 충돌 (차42-3)'
  WHEN '차43-1' THEN '차선변경 중 후방 추돌 (차43-1)'
  WHEN '차43-2' THEN '도로 후진 중 충돌 (차43-2)'
  WHEN '차43-3' THEN '회전 후 본선 진입 추돌 (차43-3)'
  WHEN '차43-4' THEN '유턴 후 진입 중 직진차 추돌 (차43-4)'
  WHEN '차44-1' THEN '주차장 통로 교차 충돌 (차44-1)'

  -- 보행자
  WHEN '차51-1' THEN '횡단보도 보행자 사고 (차51-1)'
  WHEN '차51-2' THEN '무단횡단 보행자 사고 (차51-2)'

  ELSE title
END
WHERE chart_code IN (
  '차1-1','차1-2','차2-1','차2-2','차3-1','차3-2','차4-1','차4-2','차5-1',
  '차10-1','차11-1','차11-2','차12-1','차13-1','차14-1','차15-1','차16-1','차16-2','차17-1','차20-1','차21-1',
  '차31-1','차31-2',
  '차41-1','차42-1','차42-2','차42-3','차43-1','차43-2','차43-3','차43-4','차44-1',
  '차51-1','차51-2'
);

